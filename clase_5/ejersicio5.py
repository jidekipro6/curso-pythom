# 5. Estaciones del Año
# Dado un mes (1-12), indica a qué estación pertenece.
mes = int(input("Introduce un mes (1-12): "))

if mes==1:
    print("ENERO")
elif mes ==2:
    print("FEBRERO")
elif mes ==3:
    print("MARZO")
elif  mes==4:
    print("ABRIL")
elif mes==5:
    print("MAYO")
elif mes==6:
    print("JUNIO")
elif mes==7:
    print("JULIO")
elif mes==8:
    print("AGOSTO")
elif mes==9:
    print("SETIEMBRE")
elif mes==10:
    print("OCTUBRE")
elif mes==11:
    print("NOVIEMBRE")
elif mes==12:
    print("DICIEMBRE")
else :
    print("NO EXISTE ESE MES")